def use_strings():
    #creating a string using single, double, or even triple quotes
    single_string = 'Hello'
    double_string = "hello, World!"
    triple_string = """Drongo College"""
    print(single_string) 
    print(double_string)
    print(triple_string)

    # reassigning a new string
    message = "Welcome to Drongo"
    print(message)
    message = "Hello, World" #reassigning a new string
    print(message)

use_strings()     